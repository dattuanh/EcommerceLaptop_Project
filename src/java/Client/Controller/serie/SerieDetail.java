    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        String serieId = request.getParameter("serieId");
        DaoSerie dao = new DaoSerie();
        ProductSeri s = dao.getProductSeri(serieId);
        request.setAttribute("selectedSerie", s);
        int stock = dao.getProductStock(Integer.parseInt(serieId));
        request.setAttribute("stock", stock);
        List<Image> imageList = new DaoImage().getImageList(serieId);
        request.setAttribute("imageList", imageList);
        request.getRequestDispatcher("client/detail.jsp").forward(request, response);
    } 

