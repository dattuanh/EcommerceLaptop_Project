    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {       
        String serieID = request.getParameter("serie");
        DAO dao = new DAO();
        String filter = request.getParameter("filter");
        String search = request.getParameter("search");
        ProductSeri s = dao.getProductSeri(serieID);
        request.setAttribute("selectedSerie", s);
        List<Product> productList = dao.getAllProductByFilter(filter, serieID,search);
        request.setAttribute("productList", productList);
         request.setAttribute("filter", filter);
        request.setAttribute("search", search);
        request.getRequestDispatcher("admin/product/viewSerie.jsp").forward(request, response);
    }

